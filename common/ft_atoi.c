int	ft_atoi(const char *s)
{
	int	res;
	int	sign;

	res = 0;
	sign = 1;
	if (*s == '-' || *s == '+')
	{
		sign = -(*(s) == '-') + (*(s) != '-');
		++s;
	}
	while ('0' <= *s && *s <= '9')
	{
		res = res * 10 + (*s - '0');
		++s;
	}
	return (res * sign);
}
